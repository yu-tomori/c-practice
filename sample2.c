	c4 = 4;
	c5 = 5;
	c6 = 6;
	c7 = 7;
	c8 = 8;
	c9 = 9;
	c10 = 10;

	// グローバル変数にローカル変数の値を代入する
	a1 = c1;
	a2 = c2;
	a3 = c3;
	a4 = c4;
	a5 = c5;
	b1 = c6;
	b2 = c7;
	b3 = c8;
	b4 = c9;
